package Day02.Mise;
public class Academy {
	String name;
	String code;
	int id;
	public String getName() {
		return name;
	}
	public void setName(String newName) {
		name = newName;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String newCode) {
		code = newCode;
	}
	public int getId() {
		return id;
	}
	public void setId(int newId) {
		id = newId;
	}
}